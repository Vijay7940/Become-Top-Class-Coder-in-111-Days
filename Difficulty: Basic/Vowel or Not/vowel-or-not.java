class Solution {
    String isVowel(char c) {
        // code here
        if (c == 'A' || c == 'a' || c == 'E' || c == 'e' || 
            c == 'I' || c == 'i' || c == 'O' || c == 'o' || 
            c == 'U' || c == 'u') 
        {
            return "YES";
        }
        else{
            return "NO";
        }
    }
}
